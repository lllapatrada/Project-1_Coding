def rainbow_text(text):
    colors = ["#FF69B4", "#00BFFF"]  # ‡∏™‡∏µ‡∏ä‡∏°‡∏û‡∏π‡πÅ‡∏•‡∏∞‡∏ü‡πâ‡∏≤
    colored_text = ""
    for i, char in enumerate(text):
        colored_text += f"<{colors[i % len(colors)]}>{char}</>"
    return colored_text

